# O conjunto de funções matematicas do python
# Funções min e max
# Função min retorna o valor minimo
x = min(5, 10, 52, 89,100)
print(x)

# Funciona pra listas tbm
a = [7,15,25,32,4,18]
y = min(a)
print(y)
# Função max retorna o valor maximo
print(max(x))
print(max(y))